<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">India Gate, official name Delhi Memorial, originally called All-India War Memorial, monumental sandstone arch in New Delhi, dedicated to the troops of British India who died in wars fought between 1914 and 1919. India Gate, which is located at the eastern end of the Rajpath (formerly called the Kingsway), is about 138 feet (42 metres) in height.
India Gate is one of many British monuments built by order of the Imperial War Graves Commission (later renamed Commonwealth War Graves Commission). The architect was Sir Edwin Lutyens, an Englishman who designed numerous other war memorials and was also the principal planner of New Delhi. The cornerstone was laid in 1921 by the duke of Connaught, third son of Queen Victoria. Construction of the All-India War Memorial, as it was originally known, continued until 1931, the year of the formal dedication of New Delhi as the capital of India
Lutyens declined to incorporate pointed arches or other Asian motifs in his design but strove instead for classical simplicity. <BR><BR>The result is often described as similar in appearance to the Arc de Triomphe in Paris. On the rooftop above the archway is a broad shallow domed bowl that was intended to be filled with flaming oil on ceremonial occasions. No fires have been set on the rooftop in recent years, but four eternal flames are now sheltered at the base of the structure. The flames demarcate the Amar Jawan Jyoti, a small monument that has served as India’s tomb of the unknown soldier since 1971.The cornices of India Gate are adorned with the inscription of sun which symbolized the British Imperial Colony. The word INDIA is inscribed at the top of the arches on both sides flacked by the dates MCMXIV (1914) on the left and MCMXIX (1919) on the right. Below this the following passage is inscribed – “TO THE DEAD OF THE INDIAN ARMIES WHO FELL AND ARE HONOURED IN FRANCE AND FLANDERS MESOPOTAMIA AND PERSIA EAST AFRICA GALLIPOLI AND ELSEWHERE IN THE NEAR AND THE FAR-EAST AND IN SACRED MEMORY ALSO OF THOSE WHOSE NAMES ARE HERE RECORDED AND WHO FELL IN INDIA OR THE NORTH-WEST FRONTIER AND DURING THE THIRD AFGHAN WAR”. Inscribed on other surfaces are the names of 13,218 war dead including that of a female staff nurse from the Territorial Force who was killed in action in 1917.<BR><BR>
Situated below the India Gate arch is an installation of reversed L1A1 Self-loading rifle, capped by war helmet on a plinth made in black marble. Four urns surround the structure with permanently burning flames fueled by CNG and each face of the cenotaph has the words "Amar Jawan" inscribed in gold. Named Amar Jawan Jyoti or Flame of the Immortal Soldier, it was erected in the wake of Liberation of Bangladesh in December 1971 to pay homage to Indian soldiers killed in the action. 
The memorial was inaugurated by the then Prime Minister, Indira Gandhi on 26th January, 1972. The burning flame is manned by members from the three Indian Armed Forces 24×7. Honorary wreaths are placed at the Amar Jawan Jyoti on 26 January, Vijay Diwas and Infantry Day by the Prime Minister of India and Chiefs of Indian Armed Forces<BR><BR>
The gate, which is illuminated every evening from 19:00 to 21:30, today serves as one of Delhi's most important tourist attractions. Cars travelled through the gate earlier, until it was closed to traffic.[citation needed] The Republic Day Parade starts from Rashtrapati Bhavan and passes around the India Gate.[citation needed] India gate is also a popular spot for civil society protests in New Delhi, with historical protests being against the Nirbhaya rape case, Unnao rape case, and the anti-corruption movement, inter alia.
In 2017, the India Gate was twinned with the Arch of Remembrance in Leicester, England,[further explanation needed] another Lutyens war memorial, following a very similar design but on a smaller scale. In a ceremony, India's high commissioner to the United Kingdom laid a wreath at the arch in Leicester and the British high commissioner to India laid one at the India Gate.
