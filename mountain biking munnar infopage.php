<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Cycle through some of the most breathtaking tea-growing regions of the world – this is a unique opportunity to gain an insight into the way of the world in these parts - the process of manual plucking of the tender tea leaves, the day to day life of the rural people; all the while enjoying the enchanting landscapes and the verdant stretches around. Reach Munnar in the morning. You will be met by our representative and will be briefed by the tour leader regarding the basics of cycle-mechanics as well as the itinerary. The route will be an interesting one, as you will be cycling through the narrow winding estate roads, the lush tea gardens. You will see thick patches of Shola forests. While cycling you will pass through tea plantations and their adjoining villages, and you can get a glimpse of the distinct lifestyle of the people. You will come across the local primary school, hospital, church and temple. The people cultivate vegetables and crops for their use. After about 15kms of cycling, you will reach Mattupetty dam. Near the dam, people grow grass for cattle, the grass attracting wild elephants too. On the last 5kms of downhill cycling you will enjoy the beauty of Munnar hills. <BR>
  <BR>If you want to extend the cycling, you can pedal up to ‘Top Station’ from where you can see the hills of Kerala and the plains of Tamil Nadu. The ropeway that was once used to transport tea, coffee and spices from Munnar in the olden days and the secret passage to Tamil Nadu that the British allegedly used during times of battle can also be spotted. The smiling, hard-working local population engages in simple living and is mostly tourism or plantation-dependent for employment. The endemic and endangered NIlgiri Tahr and the Neelakurinji, the little blue flower that carpets the hillsides every twelve years-are both star attractions of this hill district. The evergreen forests that surround the area, the craggy peaks that stand as sentinels and the unique flora and fauna that is peculiar to the region make it a place that nature-lovers cannot get enough of and consider a paradiseunnar hills. <BR>
  <BR>
unnar's mountainside is rich in lush forests and breathtaking natural beauty. One of the best ways to see it's finest locations through the winding pathways is to go on cycling tour with a local guide. You'll meet your guide right at the gates of Kannan Devan Hill Plantation, where you will get your tour briefing and get your bicycle for the ride. From here, you'll cycle your way through Munnar's thick forests and greenery, taking in the tranquility that the forest is known for. There will be villages where you see the locals going about their day, children playing, and you'll see the families at work. Shola forests will be all around you, referring to the patches of stunted mountain thickets and greenery, which is common for the area. You'll also be able to look through estates full of cardamom, one of the most popularly grown local spices and imported throughout the world. But one of the primary products of this area is tea, and you'll be able to witness locals at work through the rolling tea plantations. At the beautiful tea gardens themselves, you can stop and interact with the locals, hearing about the local tea and Munnar itself on a cultural level, and even take pictures of the location. 
unnar hills. <BR>
