<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">If you enjoy mountain biking, then this would be the best mountain biking trip along the hilly and curvy roads of Coorg. 

Go mountain biking in the amazing landscape of Coorg and feel the thrill of the wind on your face!
Explore the curvy roads of Coorg by mountain biking with all gears provided.
Take a Coorg coffee plantation walk and see Coorg off the beaten track
 
Coorg is located on the eastern slopes of the Western Ghats which runs parallel to the western coast of the Indian peninsula. It is a UNESCO World Heritage Site and is one of the eight “hottest hotspots” of biological diversity in the world. Coorg is known for its Coffee, Black Pepper and Cardamom, the altitude and humidity provides an opportunity to grow Coffee, Arabica and Robusta are the 2 different variants of Coffee that are grown here. The majestic mountain valleys make way for mesmerizing mountainous roads that are made for Mountain Biking. MTB can be enjoyed throughout the year except for monsoon season from June to August. The canopy above, humidity and the moderate temperature are ideal for biking.<BR><BR>
 
Biking Route Details<BR>
• Total Distance: 58km (36mi) <BR>
• Time: 5 to 6 Hours<BR>
• Terrain: Tarmac with gradual climbs and descends<BR>
• Average Temperature: 28.6 °C (83.5 °F)<BR><BR>
This triangular circuit goes through nearby town Suntikoppa (1020m) to Chettalli (800m). This is a narrow road with less traffic and Coffee estates on either side of the trail. Chettalli is a small town famous for Central Horticultural Experiment Station – one can purchase fruits, vegetables, and plants of different species. The road then leads toward the town of Madikeri (1150m) which is the district capital of the Coorg district. The road from Madikeri back to Suntikoppa is NH-275, it’s mostly downhill with relatively. The same route is taken from Suntikoppa to reach the hostel.
When considering Coorg as a mountain biking destinations then get ready for spectacular sights and mesmerizing routes to come along your way. One can mountain bike on the routes of Chelavara and Madikeri, the hilly terrains cover about 25-35kms. It's time to uncover the journey filled with evergreen forests and coffee plantations.<BR><BR> If you're looking forward to exploring historical places then take the biking route to Madikeri fort which also houses a church building, a museum, and several artifacts. You can pick a biking route to Mallali Falls which is surrounded by jungles and Pushpagiri Mountain range. Cycle in the Nilgiris along the aromatized plantations. This rural region offers some of the country’s most stunning landscapes and goes hand in hand with twisting green roads, forests, and hills.
 Mountain biking on the curvy trails of Coorg surrounded by some of the most picturesque sights is something that one tends to cherish for as long as they shall live. Coorg has plenty of routes offering an experience that is completely different from one another. While one helps you cross the hilly terrains, the other one is about passing through jungles with canopies made of towering trees and waterfalls. The fragrant tea and coffee plantations simply adds to the journey that is unmatchable.<BR><BR>
Offering some of the most surreal mountain biking trails in India, Coorg is a destination that has something for all kinds of bikers. If you are biker on the outside but a little historic buff on the inside, why not take the route along Madikeri fort that also covers other historical marvels including church buildings, museums, and numerous artifacts. The rolling velvety green hills, scenic landscapes, and the crisp winds brushing past bringing some fragrance of nearby plantations along is an experience to cherish.<BR><BR> Mountain biking in Coorg is truly an exciting experience and you can also go for camping in Coorg.
The Lush green Coorg is Paradise for all the cyclists. This hill station offers a very good weather, tall trees, magnificent roads and hypnotizing views.
Being one of the less explored hill stations, Coorg can offer you peace as you make your way across its tall green trees.
</p></strong><br>
