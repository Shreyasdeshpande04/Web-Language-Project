<strong><p style="font-size:25px">Red Fort, also called Lal Qalʿah, also spelled Lal Kila or Lal Qila, Mughal fort in Old Delhi, India. It was built by Shah Jahān in the mid-17th century and remains a major tourist attraction. The fort was designated a UNESCO World Heritage site in 2007.The fort’s massive red sandstone walls, which stand 75 feet (23 metres) high, enclose a complex of palaces and entertainment halls, projecting balconies, baths and indoor canals, and geometrical gardens, as well as an ornate mosque. Among the most famous structures of the complex are the Hall of Public Audience (Diwan-i-ʿAm), which has 60 red sandstone pillars supporting a flat roof, and the Hall of Private Audience (Diwan-i-Khas), which is smaller and has a pavilion of white marble.<BR><br>An earlier red fort had been built in Old Delhi in the 11th century by the Tomara king Anangapala. The Quṭb Mosque now stands on the site.Red Fort Delhi is the creation of architects Ustad Ahmad and Ustad Hamid. Architecturally, Red Fort or the Lal Quila surpasses many monuments of the world with its carefully planned layout and impressive structure. For instance, if the layout has to be compared, Delhi Red Fort is planned in a better way than the Red Fort of Agra. <BR><br> The tallest structure of the fort is around 33 meters high. The Delhi Red Fort is in the shape of an irregular octagon, comprising of long east and west sides along with two main gates on the western side and the southern side. The walls of the fort along with a few parts have been constructed with red sandstone while rest of the part has been built using marble. <BR><br> To prevent terrorist attacks, security is especially strict around the Red Fort on the eve of Indian Independence Day. Delhi Police and paramilitary personnel keep a watch on neighbourhoods around the fort, and National Security Guard sharpshooters are deployed on high-rises near the fort. The airspace around the fort is a designated no-fly zone during the celebration to prevent air attacks, and safe houses exist in nearby areas to which the prime minister and other Indian leaders may retreat in the event of an attack.<BR><br> The fort is open from Tuesday-Sunday; Monday closed. Tourists timing to visit the fort is from Sunrise to Sunset.<BR><br> 
 
