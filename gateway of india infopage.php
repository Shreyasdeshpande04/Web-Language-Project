<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">The Gateway of India is an arch-monument built in the early 20th century in the city of Mumbai, India. It was erected to commemorate the landing in December 1911 at Wellington Pier, Mumbai of King-Emperor George V and Queen-Empress Mary, the first British monarch to visit India. At the time of the royal visit, the gateway was not yet built, and a cardboard structure greeted the monarch.
The foundation stone was laid in March 1913 for a monument built in the Indo-Saracenic style, incorporating elements of 16th-century Gujarati architecture. The final design of the monument by architect George Wittet was sanctioned only in 1914, and construction was completed in 1924. The structure is a triumphal arch made of basalt, which is 26 metres (85 feet) high.It is an effort from the architect George Wittet.<br><br>
After its construction the gateway was used as a symbolic ceremonial entrance to British India for important colonial personnel. It has been called a symbol of "conquest and colonisation" commemorating British colonial legacy. The gateway is also the monument from where the last British troops left India in 1948, following Indian independence. It is located on the waterfront at an angle, opposite the Taj Mahal Palace and Tower Hotel and overlooks the Arabian Sea. Today, the monument is synonymous with the city of Mumbai, and is amongst its prime tourist attractions. The gateway is also a gathering spot for locals, street vendors, and photographers soliciting services. It holds significance for the local Jewish community as it has been the spot for Hanukkah celebrations, with the lighting of the menorah, since 2003. There are five jetties located at the gateway, of which two are used for commercial ferry operations.
The gateway was the site of a terror attack in August 2003, when there was a bomb blast in a taxi parked in front of it. Access to the gateway was restricted after people congregated at its premises following the 2008 Mumbai terror attacks, in which the Taj Hotel opposite the gateway and other locations in its vicinity were targeted.
In March 2019, the Maharashtra state government proposed a four-step plan to develop the location for the convenience of tourists, following a direction issued by the state governor in February 2019.
The gateway's arch has a height of 26 metres (85 feet) with its central dome being 15 metres (49 feet) in diameter.The monument is built of yellow basalt and reinforced concrete. The stones were sourced locally while the perforated screens were brought in from Gwalior.The monument faces towards the Mumbai Harbour.<br><br>There are four turrets on the structure of the gateway, and there are steps constructed behind the arch of the gateway which lead to the Arabian Sea. The monument features intricate stone latticework (also known as the jali work) The Scottish architect, George Wittet combined elements of the Roman triumphal arch and 16th-century architecture of Gujarat.The architecture also combines indigenous architectural elements with elements of Islamic architecture, known as the Indo-Saracenic. The harbour front was realigned in order to make an esplanade, which would sweep down to the centre of the town. On each side of the arch, there are large halls with the capacity to hold 600 people.The cost of the construction was ₹21 lakhs (two million one hundred thousand rupees), borne by the then government. Due to a paucity of funds, the approach road was never built. Hence, the gateway stands at an angle to the road leading up to it.
The gateway stands at an angle, opposite to the Taj Mahal Palace and Tower Hotel, which was built in 1903. In the grounds of the gateway, opposite the monument, stands the statue of Shivaji, the Maratha warrior-hero who fought against the Mughal empire to establish the Maratha empire in the 17th centuryThe statue was unveiled on 26 January 1961 on the occasion of India's Republic DayIt replaced a bronze statue of King-Emperor George V which stood at its place. In 2016, Mid-Day reported that the George V statue is kept locked in a tin shed belonging to the Public Works Department, behind Elphinstone College, in Fort, Mumbai The George V statue was sculpted by G. K. Mhatre, who has over 300 sculptures to his credit in India Hemant Pathare, Mhatre's great-grandson and Sandeep Dahisarkar, a historian, researcher, and academician, have made efforts to relocate the George V statue to a museum, the latter of whom has reasserted the statue as swadeshi art.The other statue in the locality of the gateway is that of Swami Vivekananda, an Indian monk who is credited as a key figure in the introduction of Indian philosophies such as Vedanta and Yoga to the west, and with bringing Hinduism to the status of a major world religion during the late 19th century.<br><br>
The gateway is amongst the prime tourist attractions in Mumbai.The gateway is a protected monument in Maharashtra under the aegis of the Archaeological Survey of India (ASI). It is a regular gathering place for locals, street vendors, and photographers. In 2012, the Maharashtra Tourism Development Corporation moved the Elephanta Festival of Music and Dance from its original location at Elephanta Caves — where it had been celebrated for 23 years — to the gateway, due to the increased capacity offered by the venue The gateway can host 2,000 to 2,500 people, whereas Elephanta Caves could host only 700 to 800. 
By the year 2012, the Bombay Municipal Corporation increased the plaza area around the gateway for pedestrians by restoring the area at a cost of ₹5 croresIt involved the cutting down of trees, reducing the garden area, replacing toilets, and closing the car park. The redevelopment led to a dispute between the Indian National Trust for Art and Cultural Heritage (INTACH) and the Urban Design Research Institute, and the government was criticised for poor project implementation which critics alleged had failed to conform to the original plans. <br><br>
In January 2014, Philips Lighting India, in association with the Maharashtra Tourism Development Corporation, undertook the expenditure of ₹2 crores to illuminate the gateway, by installing an LED lighting system with sixteen million shades.Philips used products from its Philips Color Kinetics and LED street lighting, and did not receive any branding for the illumination project in which 132 light points were created, which were reportedly sixty-percent more energy efficient than the old lighting system In August 2014, the state Directorate of Archaeology and Museums had proposed conservation of the gateway by the ASI, after noting deterioration caused by saline deposits from the sea.

