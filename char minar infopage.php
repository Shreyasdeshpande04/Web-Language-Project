<hr><center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">The Charminar (lit.'four minarets') constructed in 1591, is a monument and mosque located in Hyderabad, Telangana, India. The landmark has become known globally as a symbol of Hyderabad and is listed among the most recognised structures in India. It has also been officially incorporated as the Emblem of Telangana for the state of Telangana. The Charminar's long history includes the existence of a mosque on its top floor for more than 400 years. While both historically and religiously significant, it is also known for its popular and busy local markets surrounding the structure, and has become one of the most frequented tourist attractions in Hyderabad. Charminar is also a site of numerous festival celebrations, such as Eid-ul-adha and Eid al-Fitr.<BR><br> The Charminar is situated on the east bank of Musi river. To the west lies the Laad Bazaar, and to the southwest lies the richly ornamented granite Makkah Masjid. It is listed as an archaeological and architectural treasure on the official "List of Monuments" prepared by the Archaeological Survey of India. The English name is a translation and combination of the Urdu words chār and minar or meenar, translating to "Four Pillars"; the eponymous towers are ornate minarets attached and supported by four grand arches.<BR><br>Charminar, along with the Qutb Shahi Monuments of Hyderabad: the Golconda Fort, and the Qutb Shahi Tombs, were included in the "tentative list" of UNESCO World Heritage Site. The monument was submitted by the Permanent Delegation of India to UNESCO on September 10, 2010.<BR><br>The Charminar was built at the intersection of the historical trade route connecting the markets of Golkonda with the port city of Machhilipatnam. The city of Hyderabad was designed with the Charminar at its center, spread around in four quadrants along the four cardinal directions. Mir Momin Astarabadi of the Qutb Shahi dynasty played an important role and ordered extensive preparations for the design and layout along with that of the new capital city. Architects from Persia were invited to provide additional designs and suggestions.<BR><br> 
Inspired by the shapes of Shiya Tazias built to commemorate the tragic death of Prophet Muhammed’s grandson, Hussain at the battle of Karbala, the structure of the Charminar is perfectly square, with each side measuring 20m. The four grand arches open into four different streets and stand 11m wide. The square structure accommodates four minarets in each corner. The minarets are 56 meters high, house two balconies, and are topped with small delicate domes and intricate carvings on the outside walls. Unlike other prominent Islamic monuments, the minarets are built into the main structure. Inside the minarets there is a spiral staircase with 149 steps and 12 landings. The structure is a fine example of Indo-Islamic architecture with ample Persian influences. While arches and the domes show the influence of Islamic architecture, the minarets reflect Persian influence. The delicate stucco floral ornamentations on the ceiling, the balconies and the outside walls speak of Hindu influences. <BR><br>Tourists can visit Visit from 9:30 AM to 5:30 PM, all days of the week<BR><br>
 
</p></strong><br>
