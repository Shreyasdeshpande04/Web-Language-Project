The Kashi Vishwanath Temple is one of the most famous Hindu temples dedicated to Lord Shiva. It is located in Vishwanath Gali of Varanasi, Uttar Pradesh in India. The Temple stands on the western bank of the holy river Ganga, and is one of the twelve Jyotirlingas, or Jyotirlingams, the holiest of Shiva Temples. The main deity is known by the names Shri Vishwanath and Vishweshwara (IAST: Vishveshvara) literally meaning Lord of the Universe. Varanasi city was called Kashi in ancient times, and hence the temple is popularly called Kashi Vishwanath Temple. The etymology of the name Vishveshvara is Vishva: Universe, Ishvara: lord, one who has dominion.<BR><BR>

The Temple has been referred to in Hindu scriptures for a very long time as a central part of worship in the Shaiva Philosophy. It had been demolished by many Muslim rulers many times, last time It was demolished by Aurangzeb, the sixth Mughal emperor who constructed the Gyanvapi Mosque on its site. The current structure was built on an adjacent site by the Maratha ruler, Ahilya Bai Holkar of Indore in the year 1780.
Phalgun Shukla Ekadashi is celebrated as Rangabhari Ekadashi, that is, colors. According to tradition, before Holi, Baba Vishwanath comes back to Kashi after having a cow in the form of mother Bhagwati. The temple complex is echoed by the echo of dozens of Damroos. This tradition has been performed for over 200 years. On Basant Panchami Baba's Tilak is performed, Shivaratri marriage and Rangbhari Ekadashi marks parvati leaving with shiva.These traditions are carried out by the erstwhile Mahant family of the temple for over a century.
These rituals of Baba's marriage ceremony are performed at the residence of Kulpati Tiwari, the erstwhile Mahant of Shri Kashi Vishwanath Temple in Redzone. The seven rituals of Saptarishi Aarti were performed by Baba Vishwanath. According to the Puranas, Kashi is loved by the Saptarishi to the priest, so according to the tradition, the devotees of the Saptarishi Aarti perform the rituals of marriage. The seven archaks under the leadership of Pradhan Archak Pandit Shashibhushan Tripathi (Guddu Maharaj) completed the marriage in Vedic rituals<BR><BR>

Located on the banks of the holy Ganges, Varanasi is regarded among the holiest of the Hindu cities. The Kashi Vishwanath temple is widely recognized as one of the most important places of worship in the Hindu religion. Inside the Kashi Vishwanath Temple is the Jyotirlinga of Shiva, Vishveshvara or Vishvanath. The Vishveshvara Jyotirlinga has a very special and unique significance in the spiritual history of India.
Many leading saints, including Adi Sankaracharya, Ramakrishna Paramhansa, Swami Vivekananda, Bamakhyapa, Goswami Tulsidas, Swami Dayananda Saraswati, Sathya Sai Baba, Yogiji Maharaj, Pramukh Swami Maharaj, Mahant Swami Maharaj and Gurunanak have visited the site.[25][unreliable source?] A visit to the temple and a bath in the river Ganges is one of many methods believed to lead one on a path to Moksha (liberation). Thus, Hindus from all over the world try to visit the place at least once in their lifetime. There is also a tradition that one should give up at least one desire after a pilgrimage the temple, and the pilgrimage would also include a visit to the temple at Rameswaram in Tamil Nadu in Southern India, where people take water samples of the Ganges to perform prayer at the temple and bring back sand from near that temple.<BR><BR> Because of the immense popularity and holiness of Kashi Vishwanath temple, hundreds of temples across India have been built in the same architectural style. Many legends record that the true devotee achieves freedom from death and saṃsāra by the worship of Shiva, Shiva's devotees on death being directly taken to his abode on Mount Kailash by his messengers and not to Yama. The superiority of Shiva and his victory over his own nature—Shiva is himself identified with death—is also stated. There is a popular belief that Shiva himself blows the mantra of salvation into the ears of people who die naturally at the Vishwanath temple.

