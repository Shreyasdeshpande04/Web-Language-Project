<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Agra Fort is a historical fort in the city of Agra in India. It was the main residence of the emperors of the Mughal Dynasty until 1638, when the capital was shifted from Agra to Delhi. Before capture by the British, the last Indian rulers to have occupied it were the Marathas. In 1983, the Agra fort was inscribed as a UNESCO World Heritage Site.[1] It is about 2.5 km northwest of its more famous sister monument, the Taj Mahal. The fort can be more accurately described as a walled city.Like the rest of Agra, the history of Agra Fort prior to Mahmud Ghaznavi's invasion is unclear. However in the 15th century, the Chauhan Rajputs occupied it. Soon after, Agra assumed the status of capital when Sikandar Lodi (A.D. 1487–1517) shifted his capital from Delhi and constructed a few buildings in the pre-existing Fort at Agra. After the first battle of Panipat (A.D. 1526) Mughals captured the fort and ruled from it. In A.D. 1530, Humayun was crowned in it. The Fort got its present appearance during the reign of Akbar (A.D. 1556–1605).<BR><br>
Bricks formed the base of Agra Fort’s structure. Red sandstone was brought all the way from Rajasthan and that was laid on the external surfaces. Back then, the entire fort was built using the red sandstone. This appearance of the fort underwent a major change during the reign of Shah Jahan. Unlike his grandfather, Shah Jahan was smitten by the beauty of white marble. Hence, he destroyed many structures within the fort, only to rebuild them using white marble. Shah Jahan was made to spend his final days in the Musamman Burj of the Agra fort, which was built by him.The Musamman Burj is located on the left of the Khaas Mahal. It is a beautiful octagonal tower with an open pavilion. It is said that Shah Jahan used to view the Taj Mahal from this very place. <BR><br>
Agra Fort has many secret subterranean apartments and edifices. It is said that the entire fort is interconnected through tunnels and other underground pathways. According to historians, the emperors who owned the fort during their respective reigns are said to have contributed to the secret tunnels for obvious reasons. One such known tunnel is located near the Water Gate, connecting the fort to the banks of river Yamuna.
 <BR><br>
Currently the fort is a UNESCO World Heritage site. Tourists can visit the fort from Sunrise to sunset. Entry to Agra Fort is allowed through the Amar Singh Gate only.<BR><br> 
 
</p></strong><br>
