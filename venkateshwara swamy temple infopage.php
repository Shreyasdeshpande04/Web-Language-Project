<strong><p style="font-size:25px">Tirupati, city, southeastern Andhra Pradesh state, southern India. It lies in the Palkonda Hills, about 8 miles (13 km) northeast of Chandragiri and 67 miles (108 km) northwest of Chennai in Tamil Nadu state. Tirupati is known as the abode of the Hindu god Venkateshvara, Lord of Seven Hills. The sacred hill of Tirumala is situated about 6 miles (10 km) northwest of Tirupati in Sri Venkateshwara Sanctuary and National Park. The hill was considered so holy that before 1870 non-Hindus were not permitted to ascend it. At the hillâ€™s summit, at an elevation of 2,800 feet (850 metres), is a temple of great antiquity. The temple, nestled among sacred waterfalls and tanks (reservoirs) and dedicated to Venkateshvara, is a fine example of Dravidian art and is one of the most important pilgrimage centres in India. The temple is now the centre of Sri Venkateswara University 
<br><br>

Venkateswara Temple is a Hindu temple situated in the hill town of Tirumala at Tirupati in Chittoor district of Andhra Pradesh, India. The Temple is dedicated to Venkateswara, a form of Vishnu, who is believed to have appeared here to save mankind from trials and troubles of Kali Yuga. Hence the place has also got the name Kaliyuga Vaikuntha and the Lord here is referred to as Kaliyuga Prathyaksha Daivam. The temple is also known by other names like Tirumala Temple, Tirupati Temple, Tirupati Balaji Temple. Venkateswara is known by many other names: Balaji, Govinda, and Srinivasa.[3] The temple is run by body Tirumala Tirupati Devasthanams (TTD) which is under control of Andhra Pradesh Government. The head of TTD is appointed by Andhra Pradesh Government. The revenue from this shrine is used by Andhra Pradesh government. 
Tirumala Hills are part of Seshachalam Hills range. The hills are 853 metres (2,799 ft) above sea level. The Hills comprises seven peaks, representing the seven heads of Adisesha. The temple lies on the seventh peak -Venkatadri, on the southern banks of Sri Swami Pushkarini, a holy water tank. Hence the temple is also referred to as "Temple of Seven Hills". Tirumala town covers about 10.33 sq mi (26.75 km2) in area.<br><br>
The Temple is constructed in Dravidian architecture and is believed to be constructed over a period of time starting from 300 AD. The Garbagruha (Sanctum Sanctorum) is called AnandaNilayam. The presiding deity, Venkateswara, is in standing posture and faces east in Garbha gruha. The temple follows Vaikhanasa Agama tradition of worship. The temple is one of the eight Vishnu Swayambhu Kshetras and is listed as 106th and the last earthly Divya Desam. The Temple premises had two modern Queue complex buildings to organise the pilgrim rush, Tarigonda Vengamamba Annaprasadam complex for free meals to Pilgrims, hair tonsure buildings and a number of pilgrim lodging sites.
It is the richest temple in the world in terms of donations received and wealth. The temple is visited by about 50,000 to 100,000 pilgrims daily (30 to 40 million people annually on average), while on special occasions and festivals, like the annual Brahmotsavam, the number of pilgrims shoots up to 500,000, making it the most-visited holy place in the world.] In 2016, it was reported that 27.3 million pilgrims visited the temple.<br><br>
It is around 435 km (270.3 mi) from Vijayawada, 571.9 km (355.4 mi) from Hyderabad, 138 km (85.7 mi) from Chennai, 291 km (180.8 mi) from Bangalore, and 781.2 km (485.4 mi) from Visakhapatnam.
There are several legends associated with the manifestation of the Lord in Tirumala. According to one legend, the temple has a murti (deity) of Venkateswara, which it is believed shall remain here for the entire duration of the present Kali Yuga.
Medieval historyEdit
The first recorded endowment was made by Pallava queen Samavai in the year 966 CE. She donated many jewels and two parcels of land (one 10 acres and other 13 acres) and ordered to use the revenues generated from that land to be used for the celebration of major festivals in the temple.The Pallava dynasty (9th century), the Chola dynasty (10th century), and Vijayanagara pradhans (14th and 15th centuries) were committed devotees of Venkateswara. The temple gained most of its current wealth and size under the Vijayanagara Empire, with the donation of diamonds and gold. In 1517, Vijayanagara Emperor Krishnadevaraya, on one of his many visits to the temple, donated gold and jewels, enabling the Ananda Nilayam (inner shrine) roofing to be gilded. On 2 January 1517, Krishnadevaraya installed his own statue in the temple and he has made several donations to the temple.<br><br> After the decline of Vijayanagara Empire, leaders from states such as the Kingdom of Mysore and the Gadwal Samsthanam worshiped as pilgrims and gave ornaments and valuables to the temple. Maratha general Raghoji I Bhonsle (died 1755) visited the temple and set up a permanent administration for the conduct of worship in the temple.Between 1320 CA and 1369 CA, idols of Ranganatha temple of Srirangapatnam were kept in this temple for safe keeping.
The temple is considered one of the eight Swayambhu Kshetras of Vishnu where presiding deity is believed to have manifested on its own. Seven other temples in the line are Srirangam Ranganathaswamy temple, Bhu Varaha Swamy temple, and Vanamamalai Perumal Temple in South India and Saligrama in Nepal, Naimisaranya, Pushkar and Badrinath Temple in North India.
The temple is revered by Alvars in Divya Prabandham. The temple is classified as a Divyadesam, one of the 108 Vishnu temples that are mentioned in these books. The benefits acquired by a pilgrimage to Venkatachala are mentioned in the Rig Veda and Asthadasa Puranas. In these epics, Sri Venkateswara is described as the great bestower of boons. There are several legends associated with the manifestation of the at Tirumala.

