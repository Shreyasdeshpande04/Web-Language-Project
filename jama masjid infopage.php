<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Jama Masjid of Delhi, Jama Masjid also spelled Jāmiʿ Masjid, Jama Masjid of Delhi also called Masjid-i Jahān Numā, mosque in Old Delhi, India, constructed in 1650–56 by the Mughal emperor Shah Jahān, a noted patron of Islamic architecture whose most famous work is the Taj Mahal, in Agra. Jama Masjid, now the second largest mosque on the Indian subcontinent, is also an impressive example of Mughal architecture
Jama Masjid is Delhi’s principal mosque, the place where the city’s Muslims traditionally gather for Friday communal prayer; Jama Masjid is Arabic for “Friday mosque.” The mosque is near the Red Fort, yet another of Shah Jahān’s buildings. Jama Masjid and its courtyard stand on an outcropping more than 30 steps higher than the street, giving the mosque a commanding view of the surrounding area. <BR><BR>The longer name, Masjid-i Jahān Numā, translates to “world-reflecting mosque” or “world-displaying mosque.” The mosque was built by a crew of some 5,000 workers. The principal construction material was red sandstone, but some white marble was also used.
Jama Masjid is oriented toward the holy city of Mecca, Saudi Arabia, which lies to the west. An open courtyard facing the eastern gateway of the mosque building is at least 325 feet (99 metres) square and can accommodate 25,000 people. The eastern gateway itself was originally reserved for royal use exclusively. Others used smaller gates on the north and south sides of the building. Two 130-foot (40-metre) minarets mark the northeast and southeast corners of the building. The largest interior space is the prayer hall, 90 × 200 feet (27.4 × 60.96 metres). Above the prayer hall’s entrances are calligraphic inscriptions in Persian. Three large marble domes rise from the prayer hall’s roof.
The British took over Shahjahanabad in 1803. The Mughal Emperor remained the ritual imperial head of the mosque, but Mughal power and patronage had significantly waned. The initial policy of the British in the city was favourable towards its residents; the British undertook repairs and even renovations of the Jama Masjid. The Masjid continued to serve as a site of social and political discourse, in keeping with other mosques of Delhi at the time; for example, theological and philosophical debates were held between Muslims and Christians.<BR><BR>
The Revolt of 1857 was a major turning point in this situation. This event resulted in the deaths of many British people in the city, and weakened colonial authority, deeply affronting the British. It also ended the Mughal empire. The British perceived the revolt as instigated by Muslims, cultivated within Delhi's mosques. After the British reclaimed the city in the same year, they razed many mosques and banned the congregation of Muslims in any remaining mosques. The Jama Masjid fell into British confiscation during this time, and was barred from any religious use. It was repeatedly considered for destruction, but the British eventually began using it as barracks for its Sikh and European soldiers

<BR><BR>
The masjid is one of the last architectural marvels built by Shah Jahan.
 During Shah Jahan’s time, the eastern gate was used by him to access the mosque. The northern gate was meant to be used by the nobles while the southern one was open for the common people.
Shah Jahan’s son Aurangzeb drew inspiration from this mosque when designing the structural plan for the Badshahi Masjid in Lahore.
 Before the Revolution of 1857, the mosque housed a madrasa within its premises. During the revolt, it was destroyed.
In 1948, the last Nizam of Hyderabad was approached for a donation of 75,000 rupees for restoration works of one-fourth of the structure’s floor. The Nizam donated 3 lakhs rupees instead, specifying that the whole mosque floor to be renovated so that no portion should look old.
 Contrary to the popular belief, Delhi Jama Masjid is not the largest mosque in India. The Taj-ul-Masjid in Bhopal, with a capacity of 175,000 people, tops the list.
 The masjid is in straight line of Connaught Place & Parliament House
 Today, this centuries-old mosque is one of the top tourist places in Delhi. Counted among the largest mosques in the country, it draws travelers and devotees from all corners of India and beyond. The mosque witnesses a huge congregation of worshippers during Id when people gather here to offer prayers or namaz. Though there are two minarets inside the mosque, only the one at the southern end is open to the public at a nominal fee. During the evenings, the mosque is illuminated, making it a sight to behold.
 You need to dress appropriately to enter the mosque. And if you have come unprepared, robes can be hired at the northern gate.

