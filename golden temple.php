<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Harmandir Sahib, Harmandir also spelled Harimandir, also called Darbar Sahib (Punjabi: “Sacred Audience”) or Golden Temple, the chief gurdwara, or house of worship, of Sikhism and the Sikhs’ most important pilgrimage site. It is located in the city of Amritsar, Punjab state, northwestern India
The first Harmandir Sahib was built in 1604 by Arjan, the fifth Sikh Guru, who symbolically had it placed on a lower level so that even the humblest had to step down to enter it. He also included entrances on all four sides, signifying that it was open to worshippers of all castes and creeds. The foundation stone was laid by Mian Mīr, a Muslim divine of Lahore (now in Pakistan). The temple was destroyed several times by Afghan invaders and was finally rebuilt in marble and copper overlaid with gold foil during the reign (1801–39) of Maharaja Ranjit Singh. The structure thus became known as the Golden Temple.<BR><BR>
The Harmandir Sahib is the focus of a complex of buildings that form the heart of Sikhism. The temple itself occupies a small island in the centre of the tank, or pool, called the Amrita Saras (“Pool of Nectar”)—the source of the city’s name—and is connected to land on its west side by a marble causeway running across the water of the pool. Situated facing the entrance to the causeway is the Akal Takht, the chief centre of authority of Sikhism and the headquarters of the Shiromani Akali Dal (Supreme Akali Party), the main political party of the Sikhs in Punjab. On the north side of the tank is the main entrance to the complex and the Teja Singh Samudri Hall (Clock Tower), which houses the main offices of the Shiromani Gurdwara Parbandhak Committee (Supreme Committee of Temple Management), which oversees the main Sikh gurdwaras. Among several buildings on the east side of the tank are the Assembly Hall and the Guru Ram Das Langur, the latter a large dining hall that serves meals to thousands of pilgrims and other visitors each day..<BR><BR>
According to the Sikh historical records, the land that became Amritsar and houses the Harimandir Sahib was chosen by Guru Amar Das, the third Guru of the Sikh tradition. It was then called Guru Da Chakk, after he had asked his disciple Ram Das to find land to start a new town with a man-made pool as its central point. After Guru Ram Das succeeded Guru Amar Das in 1574, and in the face of hostile opposition from the sons of Guru Amar Das,[19] Guru Ram Das founded the town that came to be known as "Ramdaspur". He started by completing the pool with the help of Baba Buddha (not to be confused with the Buddha of Buddhism). Guru Ram Das built his new official centre and home next to it. He invited merchants and artisans from other parts of India to settle in the new town with him..<BR><BR>
Ramdaspur town expanded during the time of Guru Arjan financed by donations and constructed by voluntary work. The town grew to become the city of Amritsar, and the area grew into the temple complex).The construction activity between 1574 and 1604 is described in Mahima Prakash Vartak, a semi-historical Sikh hagiography text likely composed in 1741, and the earliest known document dealing with the lives of all the ten Gurus. Guru Arjan installed the scripture of Sikhism inside the new gurdwara in 1604. Continuing the efforts of Guru Ram Das, Guru Arjan established Amritsar as a primary Sikh pilgrimage destination. He wrote a voluminous amount of Sikh scripture including the popular Sukhmani Sahib

Guru Ram Das acquired the land for the site. Two versions of stories exist on how he acquired this land. In one, based on a Gazetteer record, the land was purchased with Sikh donations of 700 rupees from the owners of the village of Tung. In another version, Emperor Akbar is stated to have donated the land to the wife of Guru Ram Das..<BR><BR>
In 1581, Guru Arjan initiated the construction of the Gurdwara. During the construction the pool was kept empty and dry. It took 8 years to complete the first version of the Harmandir Sahib. Guru Arjan planned a gurdwara at a level lower than the city to emphasise humility and the need to efface one's ego before entering the premises to meet the Guru.He also demanded that the gurdwara compound be open on all sides to emphasise that it was open to all. The sanctum inside the pool where his Guru seat was had only one bridge to emphasise that the end goal was one, states Arvind-Pal Singh Mandair. In 1589, the gurdwara made with bricks was complete. Guru Arjan is believed by some later sources to have invited the Sufi saint Mian Mir of Lahore to lay its foundation stone, signalling pluralism and that the Sikh tradition welcomed all.This belief is however unsubstantiated According to Sikh traditional sources such as Sri Gur Suraj Parkash Granth it was laid by Guru Arjan himself.After the inauguration, the pool was filled with water. On 16 August 1604, Guru Arjan completed expanding and compiling the first version of the Sikh scripture and placed a copy of the Adi Granth in the gurdwara. He appointed Baba Buddha as the first Granthi..<BR><BR>
Ath Sath Tirath, which means "shrine of 68 pilgrimages", is a raised canopy on the parkarma (circumambulation marble path around the pool). The name, as stated by W. Owen Cole and other scholars, reflects the belief that visiting this temple is equivalent to 68 Hindu pilgrimage sites in the Indian subcontinent, or that a Tirath to the Golden Temple has the efficacy of all 68 Tiraths combined The completion of the first version of the Golden Temple was a major milestone for Sikhism, states Arvind-Pal Singh Mandair, because it provided a central pilgrimage place and a rallying point for the Sikh community, set within a hub of trade and activity.
</p></strong><br>
