<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Ancient texts hold this abode of Lord Vishnu at a very high esteem, stating - "there are many spots of pilgrimage in the heaven, earth and the world, but there has been none equal to Badri, nor shall there be." At the height of 3,133 meters, it stands proud with its rich past along the banks of Alaknanda River. Regarded as the most important temple of Vishnu, the Badrinath shrine is said to be established by Adi Shankaracharya. He found the Saligram idol of Lord Badri immersed in the waters of Alaknanda and installed it in a cave near the Tapt Kund.
It was in the 16th century that a Garhwal King got a temple erected to house the statue of the God. The present structure is the result of plenty of restorations the temple has undergone due to damages caused by avalanches and earthquakes. Lord Vishnu can be worshipped in a black stone idol sitting in a meditative pose surrounded by idols of other gods like Nar, Narayana, Narad, Ganesha, Garud and Kuber. In the winter season, the idol of Lord Badri is shifted to Yogadhyan Badri in Pandukeshwar (Chamoli district).<BR><BR>
The name of the Badrinath tirtha originates from the local word badri which is a type of a wild berry. It is said that when Lord Vishnu sat in penance in these mountains, His consort Goddess Laxmi took the form of a berry tree and shaded Him from the harsh sun. It is not only the dwelling place of the Lord Himself but also home to countless pilgrims, saints and sages, who meditate here in search of enlightenment.
According to Skand Puran the idol of Lord Badrinath was recovered by Adiguru Shankaracharya from Narad Kund and was re-enshrined in the 8th century A.D. in this temple. Skanda Purana describes more about the place: “There are several sacred shrines in heaven, on earth, and in hell; but there is no shrine like Badrinath
According to mythics, Badrinath oftenly called as Badri Vishal, was re-established by Adi Shri Shankaracharaya to revieve the lost prestige of Hinduism and to unite nation in one bond. Badrinath is one land richly infused with sacred accounts from numerous ancient Hindu scriptures. Be it the puranic story of the Pandav brothers, along with Draupadi, going past on their last pilgrimage by ascending the slopes of a peak near Badrinath called Swargarohini or the ‘Ascent to Heaven’ or the visit by Lord Krishna and other great sages, these are just some of the many tales which we associate with this holy tirtha.
According to Vamana Purana, the sages Nara and Narayana (fifth avatar of Lord Vishnu) perform Penances here.
Great sages of yore like Kapila Muni, Gautam, Kashyap have performed penance here, Bhakta Narada attained salvation and Lord Krishna loved this region, medieval religious scholars like Adi Shankaracharya, Ramanujacharya, Sri Madhavacharya, Sri Nityananda have come here for learning and quiet contemplation and so many still continue to do even today.<BR><BR>

The temple is also one of the 108 Divya Desams dedicated to Vishnu, who is worshipped as Badrinath—holy shrines for Vaishnavites. It is open for six months every year (between the end of April and the beginning of November), because of extreme weather conditions in the Himalayan region. The temple is located in Garhwal hill tracks in Chamoli district along the banks of Alaknanda River.It is one of the most visited pilgrimage centers of India, having recorded 1,060,000 visits.
The most prominent festival held at Badrinath Temple is Mata Murti Ka Mela, which commemorates the descent of the river Ganges on mother earth. The mother of Badrinath, who is believed to have divided the river into twelve channels for the welfare of earthly beings, is worshiped during the festival. The place where the river flowed became the holy land of Badrinath
The Badri Kedar festival is celebrated during the month of June in both the temple and the Kedarnath temple. The festival lasts for eight days; artists from all over the country perform during the function.
The major religious activities (or pujas) performed every morning are mahabhishek (ablution), abhishek, gitapath and bhagavat puja, while in the evening the pujas include geet govinda and aarti. Recital in vedic scripts like Ashtotram and Sahasranama is practised during all the rituals. After aarti, the decorations are removed from the image of Badrinath and sandalwood paste is applied to it. The paste from the image is given to the devotees the next day as prasad during the nirmalaya darshan. All the rituals are performed in front of the devotees, unlike those in some Hindu temples, where some practices are hidden from them. Sugar balls and dry leaves are the common prasad provided to the devotees. From May 2006, the practise of offering Panchamrit Prasad, prepared locally and packed in local bamboo baskets, was started.
<BR><BR>
As indicated in the temple records, the priests of the temple were Shiva ascetics called Dandi Sanyasis, who belonged to Nambudiri community, a religious group common in modern Kerala. When the last of the ascetics died without an heir in 1776 AD, the King of Garhwal invited non-ascetic Nambudiris from Kerala for the priesthood, a practice that continues in modern times. Till 1939, all the offerings made by the devotees to the temple went to the Rawal (Chief Priest), but after 1939, his jurisdiction was restricted to religious affairs.  The administrative structure of the temple consists of a chief executive officer who executes the orders from the state government, a deputy chief executive officer, two OSDs, an executive officer, an account officer, a temple officer, and a public officer to assist the chief executive officer. 


