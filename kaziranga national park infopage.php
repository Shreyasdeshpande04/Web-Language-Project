<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Kaziranga National Park is one of the prides of India. Kaziranga is the place where the nature unwinds its pristine form in millions of hues, where wildlife roams fearlessly, where man and nature meets together. This prestigious national park of India is situated in the north eastern part of the country in the district of Golaghat and Nagoan in the state of Assam. Let’s go back to the beginnings of the twentieth century in the year of 1904 when this beautiful wildlife sanctuary was created, and about 68 years later it was declared as National Park.<BR><br>Rich Wildlife - Kaziranga National Park is one of the major wildlife tourist’s attractions in India. Thousands of travellers from different part of the country and world come here every year to unlock the doors of diverse wildlife and scout the exquisiteness of the natural empire. The perfect topography with a river and its varied grasslands winds through its entire length, Kaziranga promises an adventure that you will never forget.<BR><br>One-Horned-Rhino - The tourism of Kaziranga is not just about spotting one of the many one-horned rhinoceros or tigers but also to watch the three giant herbivores that reside in the park called – The Asiatic Elephant, The Swamp Deer and the Asiatic Water Buffaloes. The park has also been demarcated as an important bird area by the Birdlife International in order to promote the conservation of endangered species.<BR><br>Elephant Ride - One of the many Attractions in the Kaziranga National Park is exploring the hidden mystery of the dense jungle on elephants that are steered by the professional Mahout. It is a special treat to the travellers to move in the open grounds on these majestic animals. Safaris can be booked in advance from the online website link given.<BR><br>Jeep Safari - The other available options to see the national park is on booked jeep safari. Taking jeep safari ride in the jungles of Kaziranga is an apex form of tourism. Open gypsy can accommodate six tourists at a time. It takes you to the unexplored part of the part.<BR><br>The weather experienced in Kaziranga National Park is typically tropical. The summer is very hot and humid (April - June), and cold nights in winters (November - January). The park gets flooded with the water of Brahmaputra River during monsoon every year. The rainy season starts from June and continues to August. Park remains closed during these seasons. The months of November to April are the best time to visit Kaziranga National Park.<BR><br>Jeep Safari Timings: Morning Jeep Safari: 07:00 – 09:30, Afternoon Jeep Safari: 13:30 – 15:30. Elephant Safari: Start Very early morning and continues for 1 – 1 & 1/2 hrs.
