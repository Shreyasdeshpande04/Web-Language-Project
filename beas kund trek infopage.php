<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">The Beas Kund, which is regarded as a holy lake is located in the Kullu Valley and is the original source of River Beas. It is believed that Rishi Vyas used to take his daily bath in the pristine water of this lake that is perched at an altitude of 3,650 meters in the lap of Mt. Hanuman Tibba and Seven Sisters. Snaking and passing through the Solang valley the trail to Beas Kund offers a rejuvenating experience and the journey is filled with several surprises at each turn. The Beas Kund Trek, which is considered as one of the easiest treks in the Himachal Himalayan region is also a short trek of three days from the popular hill station of Manali that is tucked at a height of 2,050 meters.<BR><br>
The Beas Kund Trek commences from Manali and leads to Dhundi at 3,150 meters via Solang Nallah. The trail faces a gradual climb from Solang Nallah and it takes approximately 5 â€“ 6 hours. The route from Dhundi winds upward to Bakarthach, which is at an elevation of 3,300 meters and leads to Beas Kund after a gradual climb over the moraine. The best time for Beas Kund Trek is between Mid-May and Mid-October. The weather remains quite salubrious making the trek easy and ofcourse thrilling.<BR><br>
It is a two to three days trek and can be comfortably accommodated during a long weekend. Some trekkers do it in a single day (from Dhundi to Dhundi). It entirely depends on how much you choose to walk in a day and whether you like to take it slow while enjoying the surroundings. If time is a constraint and you are a good hiker, it is doable in a day.
But one of the reasons trekkers love this trek is the setting. Picture this: Lush green grasslands flourish below the summits of the three biggest mountains around Manali. And hidden in these grasslands is the emerald lake out of which the river Beas emerges.
The contrasting sight of this serene water body nestled in between jagged mountain peaks of Pir Panjal range adds to the beauty of his setting.<BR><br>
Standing here, there is also a keen sense of being near the source of the raging river you see in the valley below. This emerald kund branches off into streams, tumbling waterfalls right until it converges into the boiling Beas river.
As is the case with every river that feeds a civilisation or settlements surrounding it, the Beas Kund is also associated with legend.
 
</p></strong><br>
