<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Thar Desert, also called Great Indian Desert, arid region of rolling sand hills on the Indian subcontinent. It is located partly in Rajasthan state, northwestern India, and partly in Punjab and Sindh (Sind) provinces, eastern Pakistan.The Thar Desert covers some 77,000 square miles (200,000 square km) of territory. It is bordered by the irrigated Indus River plain to the west, the Punjab Plain to the north and northeast, the Aravalli Range to the southeast, and the Rann of Kachchh to the south. The subtropical desert climate there results from persistent high pressure and subsidence at that latitude. The prevailing southwest monsoon winds that bring rain to much of the subcontinent in summer tend to bypass the Thar to the east. The name Thar is derived from thul, the general term for the region’s sand ridgesThe Thar’s desert sands overlie Archean (early Precambrian) gneiss (metamorphic rocks formed between 4 billion and 2.5 billion years ago), Proterozoic (later Precambrian) sedimentary rocks (formed about 2.5 billion to 541 million years ago), and more-recent alluvium (material deposited by rivers).<BR><BR> The surface consists of aeolian (wind-deposited) sand that has accumulated over the past 1.8 million years.The desert presents an undulating surface, with high and low sand dunes separated by sandy plains and low barren hills, or bhakars, which rise abruptly from the surrounding plains. The dunes are in continual motion and take on varying shapes and sizes. Older dunes, however, are in a semi-stabilized or stabilized condition, and many rise to a height of almost 500 feet (150 metres) above the surrounding areas. Several playas (saline lake beds), locally known as dhands, are scattered throughout the region.
The grasses form the main natural resources of the desert. They provide nutritive pasturage as well as medicines used locally by the inhabitants. Alkaloids, used for making medicine and oils for making soap are also extracted. There are five major breeds of cattle in the Thar. Among those the Tharparkar breed is the highest yielder of milk, and the Kankre breed is good both as a beast of burden and as a milk producer. Sheep are bred for both medium-fine and rough wool. Camels are commonly used for transport as well as for plowing the land and other agricultural purposes. Where water is available, farmers grow crops such as wheat and cotton.<BR><BR>
Best time to visit the Thar Desert is in the cool months of October-February. The climate in the summers is extremely hot and unbearable for those who are not used to it. In the winters, the days are pleasant and the winters extremely cold, but better than navigating the sands in the heat
The Desert cities of Rajasthan includes the golden city-Jaisalmer ,pink city-jaipur, blue city-Jodhpur, Bikaner and city of lakes Udaipur. Among these desert cities, Jaisalmer is known as the jewel of desert and famous for its amazing fort,camel safari,golden sand dunes and colorful culture. The Jaisalmer Fort is one of the oldest living fort in India and display the true image of the Royalty of the city.
The Rajasthan desert festival organized every year in winter and celebrated by the people of the desert with dance and singing. <BR><BR>During the desert festival and fairs, colorful folk culture of royal Rajasthan can be seen, the snake charmers,folk performers,puppeteers, art and crafts are the major attraction of the festival.
The best way to explore the Thar region of India is using the Camel Safari and jeep safari,Though the jeep safari in not as much as popular as the camel safari.Camel Safari around the desert city of Jaisalmer is one of the most visited and major attraction of the city along with its amazing fort. The adventures Camel Safari is the best and unique way to explore the desert region, its color and culture.


