<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Taj Mahal is the epitome of grandeur and splendour, you cannot fathom the beauty of this white marbled monument unless you stand in front of it. As you approach Taj Mahal, it gradually becomes known to you that this is no ordinary monument, but a vision come to life! Moved by its beauty and the deep affection of Shah Jahan for Mumtaz, the Nobel Laureate Rabindranath Tagore called it "a teardrop on the face of eternity". A UNESCO World Heritage Site, it is located on the bank of the Yamuna River and is counted among the most popular heritage monuments in India.
Built between 1631 and 1648 AD, the Taj Mahal stands as the finest example of Indo-Islamic architecture, and is one of the seven wonders of the world. The timeless beauty of the Taj is a reflection of the profound pain and longing which Shah Jahan was overcome by after the death of Mumtaz Mahal. Today, visitors celebrate, reflect and admire the splendour of the Taj. Inside, both Shah Jahan and Mumtaz lie buried in their graves, divided in life, but united in death.<BR><BR>
There is nothing ordinary about the Taj. From its minarets which are always bent curiously outwards to its inlay work, calligraphy, gateways, panels and arches, every nook and corner is a manifestation of divine grace that its builder seems to have been blessed with. Every intricate detail that catches your eye is worthy of being described in thousands way for its exquisite beauty. Unsurprisingly, Taj Mahal always occupies the first place in the heritage tourism attractions in Uttar Pradesh.
Architecture
The Taj Mahal is the finest example of Indo-Persian architecture. It incorporates Persian, Indian and Islamic architectural styles. The main gateway of the Taj Mahal, which is the most photographed section of the monument, consists of a door carved from solid silver. It is believed that this door was built to prevent the tomb which is located inside from being visible from anywhere. The tomb, therefore, is only visible when you have arrived at the gateway itself. Look up though and you will find the ceiling decorated with verses from the Holy Quran in wonderful calligraphy.
The tomb itself is built in a rectangular shape and lies at the northern end of the Taj Mahal. While the ceiling is decorated with floral patterns, the floors and other areas of the room are adorned with floral designs. Here, it is interesting to know that the tombs visible to the public and tourists are not the real tombs of Shah Jahan and Mumtaz. <BR><BR>The real ones are protected in a crypt.
One feature of the Taj Mahal which makes its presence everywhere are the lily flowers. This suggests the love of the Mughals for gardens. Indeed, some of the best gardens in India were built by the Mughals. The central hall is surrounded by eight rooms which are connected by a common corridor.
No description of the architecture of the Taj Mahal is complete without mentioning its dome. It stands like a crown on a king and plays a big role in lending this monument a feeling of eternity. Bulbous shaped, the outer doom stands at a height of almost 44.4m, while the inner dome stands at a height of almost 24.3m. It is popularly believed that this dome was built by Ismail Afandi from Turkey, a renowned palace dome designer in Turkey. Here too, the combination of Hindu and Islamic styles of architectures is visible in the form of “chhatris” (an umbrella like structure), which is a feature mostly found in temples.<BR><BR>
Interesting Facts about Taj Mahal<BR>
• The construction of the Taj Mahal took around 22,000 painters, stonecutters, embroidery artists and labourers.<BR>
• Around 1,000 elephants were used to transport the materials to the construction site.<BR>
• Shah Jahan wanted to build a black Taj Mahal which would stand across from the original one. However, before he could do so, he was deposed by his own son Aurangzeb.<BR>
• The minarets are leaning away from the monument in order to prevent any damage to it in the event of a collapse.<BR>
• It took 17 years to complete the building of the Taj Mahal.
<BR><BR>
Taj Mahal, one of the top heritage attractions of India, can be visited throughout the year. During summer, it can get quite hot. If you are brave enough to face the heat, then you can visit it without any problem. However, the cool and pleasant weather during winters creates the perfect conditions for visiting it. Taj Mahal opens 30 minutes before sunrise and closes 30 minutes before sunset. It is closed for general viewing to all visitors on Fridays.
In its harmonious proportions and its fluid incorporation of decorative elements, the Taj Mahal is distinguished as the finest example of Mughal architecture, a blend of Indian, Persian, and Islamic styles. Other attractions include twin mosque buildings (placed symmetrically on either side of the mausoleum), lovely gardens, and a museum. One of the most beautiful structural compositions in the world, the Taj Mahal is also one of the world’s most iconic monuments, visited by millions of tourists each year. The complex was designated a UNESCO World Heritage site in 1983.<BR><BR>
Over the centuries the Taj Mahal has been subject to neglect and decay. A major restoration was carried out at the beginning of the 20th century under the direction of Lord Curzon, then the British viceroy of India. More recently, air pollution caused by emissions from foundries and other nearby factories and exhaust from motor vehicles has damaged the mausoleum, notably its marble facade. A number of measures have been taken to reduce the threat to the monument, among them the closing of some foundries and the installation of pollution-control equipment at others, the creation of a parkland buffer zone around the complex, and the banning of nearby vehicular traffic. A restoration and research program for the Taj Mahal was initiated in 1998. Progress in improving environmental conditions around the monument has been slow, however.
Strict security is in place at the Taj Mahal, and there are checkpoints at the entrances. Your bag will be scanned and searched. Large bags and day packs aren't allowed to be taken inside.<BR><BR> Only small bags containing essential items are permitted. This includes one cell phone, a camera, and a water bottle per person. You can't bring edibles, tobacco products or lighters, electrical items (including phone chargers, headphones, iPads, torches), knives, or camera tripods inside. Cell phones are also banned during night viewing sessions, although cameras are still allowed.
Taj Mahotsav takes place at Shilpgram in Agra, right near the Taj Mahal, from February 18-27 each year. The focus of this festival is on arts, crafts, Indian culture, and recreating the Mughal Era. It gets underway with a spectacular procession that includes elephants, camels, and drummers. Camel rides are on offer, and there are also games for the kids and a food festival. The venue has special significance, as it's apparently located on the site where the artisans who built the Taj Mahal once lived.

