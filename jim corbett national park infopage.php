<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Jim Corbett National Park is one of the most celebreated wildlife reserve in India. This park is spread in Nainital, Pauri and Almora district. Corbett National Park is an amazing blend of high hills, swamp, River side belts and green meadows covering an area of 522 lilometers at the foot hills of the great Himalayas and this makes the park a heaven for wildlife adventures lovers. This park is famous for variety of rare flora, wild animals, reptiles and birds species. A pathway adorn with mango orchards leads one into a complete different world housing a record number of 488 different species of plants and 580 birds type. Moreover a plethora of 25 reptile species and 50 species of mammals are reasons enough to go personal with this wonderland. Visitors are allowed May to June to visit Corbett National Park.<BR><br>Jim Corbett National Park was founded in 1936, as the “Hailey National Park”. Later it has changed to “Jim Corbett National Park” in the honour of fabled hunter-“Jim Corbett” is well known for his book “The Man-Eaters of Kumaon”, and was greatly revered by local people for shooting tigers that had developed a liking for human flesh in between 1907 to 1939. Jim Corbett National Park had selected India’s first national park to start “Project Tiger” to conserve the ecosystem and wildlife, especially tigers.<BR><br>Jim Corbett National Park is located at Ramnagar town in Nainital District and it’s surroundings are spread in Pauri and Nainital district. Corbett Park is famous for its wide variety of wildlife and its beautiful location at the foothill of Himalaya by the Ramganga River. With the recent inclusion of the Sonanadi Wildlife Sanctuary to the west, Corbett has grown from 520 to 1318.5 sq km.Jim Corbett National Park is one of the majestic place for the Nature and Wildlife lovers. In this park Elephant Safari and Jeep Safari are the most favourite activities. This park is situated on the bank of Ramganga River. Forests of Sal are very famous for this area. In this park more than 600 birds, 50 mammals, and 25 reptiles’ species. Special attractions of this park are wild Elephants and Royal Bengal Tigers. Corbett Park completely opens for tourists 15th November to 15th Jun every year.<BR><br>More commonly seen wildlife includes the wild elephant, Langur monkey (black face, long tail), rhesus macaque, peacock, and several type of deer including Chital (spotted deer), Sambars, hog deer and barking deer. There are also crocodiles, the odd-looking Gavial or Gharial, monitor lizards, wild boars and jackals. Leopards (referred to as panthers in India) are occasionally seen.Corbett is also a birds watchers paradise, and since the creation of the Kalagarah Dam on the Ramganga River, large numbers of waterfowl have been attracted here.The most popular time to visit Jim Corbett National Park is summer from 6am to 9am and 3am to 6am and in winter from 7am to 10am and 2am to 5am.
</p></strong><br>
