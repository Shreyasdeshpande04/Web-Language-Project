<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Maharashtra's oldest and largest National Park, the "Tadoba National Park", also known as the "Tadoba Andhari Tiger Reserve" is one of India's 47 project tiger reserves existing in India. It lies in the Chandrapur district of Maharashtra state and is approximately 150 km from Nagpur city. The total area of the tiger reserve is 1,727 Sq.km, which includes the Tadoba National Park, created in the year 1955. The Andhari Wildlife Sanctuary was formed in the year 1986 and was amalgamated with the park in 1995 to establish the present Tadoba Andheri Tiger Reserve. The word 'Tadoba' is derived from the name of God "Tadoba" or "Taru," which is praised by local tribal people of this region and "Andhari" is derived from the name of Andhari river that flows in this area.<BR><br>The Tadoba National Park is divided into three separate forest ranges, i.e. Tadoba north range, Kolsa south range, and Morhurli range, which is sandwiched in between the first two. There are two lakes and one river in the park, which gets filled every monsoon, the ‘Tadoba Lake,’ ‘Kolsa Lake,’ and ‘Tadoba River.’ These lakes and rivers provide vital ingredients needed to sustain the park’s life.<BR><br>The Tadoba Tiger Reserve is rich in flora and fauna. Some of the famous and wildly seen flora of this park include, Teak, Ain, Bija, Dhauda, Hald, Salai, Semal, Tendu, Beheda, Hirda, Karaya gum, Mahua Madhuca, Arjun, Bamboo, Bheria, Black Plum, and many others. Apart from this the list of animals noted in this part include, Tigers, Indian leopards, Sloth bears, Gaur, Nilgai, Dhole, Striped Hyena, Small Indian Civet, Jungle Cats, Sambar, Spotted Deer, Barking Deer, Chital, Marsh Crocodile, Indian Python, Indian Cobra, Grey-headed Fish Eagle, Crested Serpent Eagle, Peacock, Jewel Beetles, Wolf Spiders, etc.<BR><br>The vegetation of Tadoba forest is of Southern tropical dry deciduous type and is spread on around 626 sq.km.Teak is the prominent tree species in the forest and there are a few lakes, which makes sure the park has rich water resources.Major part of this forest lies in the hilly area, thus many hillocks and terrains provide shelter to the wild animals here.It has a dense forest area, smooth meadows, deep valleys, and a great atmosphere to stabilize more numbers of tigers.The main attraction of the Tadoba National Park is Jungle or Tiger Safari in an open top gypsy.There is good chance to spot the shy Sloth Bear and Wild Dogs.One can find some of the Central India’s best native woodland bird species in this forest.Accommodation facilities are available and are mainly concentrated among two of its popular entrance gates i.e. Kolara Gate and Mohurli Gate.<BR><br>The park is open for visitors from 15th October to 30th June every season and remains full-day closed on every Tuesday.
</p></strong><br>
