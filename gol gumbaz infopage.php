<center><h2>INFORMATION</h2></center><BR>
<strong><p style="font-size:25px">Gol Gumbaz is the tomb of king Mohammed Adil Shah, Adil Shahi Dynasty. Construction of the tomb, located in Bijapur , Karnataka, India, was started in 1626 and was completed in 1656. The name is based on "Gol Gumbadh" derived from "Gola Gummata" meaning "circular dome". It follows the style of culture and [Indo-Islamic architecture] . Even a slight whisper by someone standing in its gallery can be heard everywhere else in the gallery, and if somebody claps, the sound of it echoes several times.<BR><br>
Gol Gumbaz is an architectural triumph of Deccan dynasty. This historic monument is designed by Yakut of Dabul. The Gol Gumbaz is composed of cube and capped by a dome. It is a world’s largest single chamber structure enclosing 17,000 m2. Four octagonal shaped towers with 7 floors each are standing at four corners of cube. The monument is constructed using Dark grey basalt material. The astonishing architecture of Bijapur Gol Gumbaz makes it stand out from the rest in world. The Gol Gumbaz, also known as Gol Gumbadh, was designed by an architect named Yaqut of Dabul. Dabul, also known as Dabhol, is a small seaport town in Ratnagiri District, Maharashtra, India. The mausoleum has been built in dark grey Basalt stone and the facade is decorated with plaster. It is cohabited in a complex with other structures like a dharamshala (a simple inn), a mosque and other buildings along with a beautiful, well-maintained garden.The architectural style of the building is Deccan Indo-Islamic which is a perfect confluence of Indo-Islamic and Dravidian architecture. Deccan rulers produced an independent style of their own, largely ignoring the locally predominant architectural styles and, were primarily influenced by the Persian and Mughal architectural nuances.<BR><br>
The huge dome capping the cube is not supported by even a single pillar. Two rotated square creates eight interlocking intersection point which are the only support of the gigantic dome. The dome has an outer diameter of 144 feet, making it world’s second largest dome. The place will surprise you with more hidden history treasure.Tourists can visit from 10:00 AM to 5:00PM, all days of the week.<BR><br> 
 
